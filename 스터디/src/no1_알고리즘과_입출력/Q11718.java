package no1_알고리즘과_입출력;

import java.util.Scanner;

public class Q11718 {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		while(sc.hasNextLine()) {
			String str=sc.nextLine();
			//sc.next()->띄어쓰기 줄바꿈
			//sc.nextLine()->한줄 그대로 출력
			System.out.println(str);
		}

	}

}
